# Contagem de ocorrências

itens = ["mouse", "teclado", "mouse", "monitor", "mouse", "teclado"]

mouse = itens.count("mouse")
teclado = itens.count("teclado")
monitor = itens.count("monitor")

print(f"O mouse aparece {mouse} vezes")

print(f"O teclado aparece {teclado} vezes")

print(f"O monitor aparece {monitor} vez")