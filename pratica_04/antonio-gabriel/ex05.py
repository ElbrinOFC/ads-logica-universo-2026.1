# Percurso simples com for

frutas = ["maçã", "banana", "uva", "pera"]

# percorre a lista
for fruta in frutas:
    print("Eu gosto de", fruta)

# mostra a quantidade de frutas
print("Quantidade de frutas:", len(frutas))


