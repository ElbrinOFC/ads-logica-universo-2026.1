comentario = "Ótimo desempenho, continue praticando!"
# Justificativa: é um texto corrido, melhor representado como string.

# (d) Tarefas de um projeto → lista
tarefas = ["Pesquisar tema", "Escrever relatório", "Revisar conteúdo", "Apresentar trabalho"]
# Justificativa: são várias atividades distintas, cada uma deve ser um item da lista.

