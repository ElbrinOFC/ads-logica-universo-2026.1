# 1. Criar lista vazia
tarefas = []

# 2. Adicionar pelo menos quatro tarefas
tarefas.append("Estudar Python")
tarefas.append("Resolver exercícios")
tarefas.append("Revisar código")
tarefas.append("Enviar atividade")

# 3. Exibir a lista final
print("Lista de tarefas:", tarefas)

# 4. Exibir o total de tarefas cadastradas
print("Total de tarefas:", len(tarefas))

