palavra = "algoritmo"
notas = [7.0, 8.5, 6.0, 9.0, 7.5]

# 1. Primeira letra da palavra
print("Primeira letra:", palavra[0])

# 2. Quarta letra da palavra
print("Quarta letra:", palavra[3])

# 3. Primeira nota da lista
print("Primeira nota:", notas[0])

# 4. Última nota da lista
print("Última nota:", notas[-1])

# Desafio opcional:
# Em Python, o primeiro índice é 0 porque a contagem começa do zero.
# Assim, palavra[0] acessa o primeiro caractere e notas[0] acessa o primeiro elemento da lista.
