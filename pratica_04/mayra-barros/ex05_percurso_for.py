frutas = ["maçã", "banana", "uva", "pera"]

# 1. Percorrer a lista com for
for fruta in frutas:
    print(f"Eu gosto de {fruta}")

# 2. Exibir a quantidade de frutas percorridas
print("\nQuantidade de frutas:", len(frutas))

