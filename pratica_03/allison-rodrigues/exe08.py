
# Qual era o erro?

# Faltava o dois-pontos (:) no final da linha da definição da função def saudacao(nome).

# Por que o programa não executava?
# Em Python, toda definição de função precisa terminar com : para indicar o início do bloco de código da função.

# Sem esse símbolo, o interpretador não sabe onde começa o corpo da função e gera um SyntaxError.